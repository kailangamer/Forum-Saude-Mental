            <h2>Quem somos?</h2>
            <p> Bom!! Somos alunos da Escola Técnica de Ceilândia e desenvolvemos esse projeto com o intuito conseguir o diploma de Técnico em informática. Este projeto foi planejado desde o começo do ano de 2023, passamos praticamente 70% desse tempo pensando e organizando como funcionaria esse trabalho. A um detalhe  importante, estava sendo desenvolvido um sistema web ou melhor um site. E para decidir o tema foi uma luta, mas depois de horas de debate entre o grupo , foi decidido fazer um fórum. Mas não é qualquer fórum e sim um fórum focado na saúde mental. Escolhemos esse tema por que é ele essencial para nossa sobrevivência  mas aparentemente não há reconhecimento merecido pela sociade e/ou pelo governo. Logo com o objetivo de nos tornarmos os mocinhos que pensam nas pessoas que passam por problemas que afetam a saúde mental,desenvolvemos esse sistema conhecido pelo Brasil todo o "FSM-Fórum Sáude Mental" . </p>
            <p> Se tiver interesse nesse projeto basta entrar em contato pelo email que está localizado no rodapé desta pagina. Agradeço a atenção.E saiba que se você precisar conversar ou de alguma informação sobre saúde mental. As portas estão abertas pra você ou melhor o sistema está aberto para sua visita.</p>
